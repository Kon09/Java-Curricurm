package data;

public class Data {
	private String animal;
	private double size;
	private int speed;
	
	public void setAnimal(String animal) {
		this.animal = animal;
	}
	
	public void setSize(double size) {
		this.size = size;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	public String getAnimal() {
		return this.animal;
	}
	
	public double getSize() {
		return this.size;
	}
	
	public int getSpeed() {
		return this.speed;
	}

}
